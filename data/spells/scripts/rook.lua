local combat = createCombatObject()
setCombatParam(combat, COMBAT_PARAM_EFFECT, CONST_ME_MAGIC_GREEN)
setCombatParam(combat, COMBAT_PARAM_AGGRESSIVE, false)

local condition = createConditionObject(CONDITION_ATTRIBUTES)
setConditionParam(condition, CONDITION_PARAM_TICKS, 200000000)
setConditionParam(condition, CONDITION_PARAM_SKILL_MELEEPERCENT, 400)
setConditionParam(condition, CONDITION_PARAM_SKILL_SHIELDPERCENT, 400)
setConditionParam(condition, CONDITION_PARAM_SKILL_DISTANCEPERCENT, 400)
setConditionParam(condition, CONDITION_PARAM_STAT_MAGICPOINTS, 100)
setConditionParam(condition, CONDITION_PARAM_MANAGAIN, 20)
setConditionParam(condition, CONDITION_PARAM_MANATICKS, 20)
setConditionParam(condition, CONDITION_PARAM_HEALTHGAIN, 20)
setConditionParam(condition, CONDITION_PARAM_HEALTHTICKS, 20)
setConditionParam(condition, CONDITION_PARAM_BUFF, true)
setCombatCondition(combat, condition)

function onCastSpell(cid, var)
	return doCombat(cid, combat, var)
end
